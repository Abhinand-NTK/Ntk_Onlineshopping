function toggleReturnReason() {
    var returnReasonBox = document.getElementById('return-reason-box');
    if (returnReasonBox.style.display === 'none') {
        returnReasonBox.style.display = 'block';
    } else {
        returnReasonBox.style.display = 'none';
    }
}

